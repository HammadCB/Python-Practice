class Employee: # Class Creation
    language = "Python"# Class attribute
    salary = 50000

Hammad=Employee() # Object Creation
Hammad.name="Hammad" # Instance Variable
print(Hammad.name,Hammad.language,Hammad.salary) # Accessing Class Variable

Ali=Employee() 
Ali.name="Ali" #Object Attribute
print(Ali.name,Ali.language,Ali.salary) 

